<h3>Comment l\'utiliser&nbsp;?</h3>
<p>Ce module n\'a pas de partie publique, il est visible dans le panneau d\'administration.  Il vous permet de sélectionner un gabarit (actif), et de cliquer "Exporter".  Un fichier XML contentant le gabarit et ses feuilles de style attachées sera créé et proposé au téléchargement.</p>
